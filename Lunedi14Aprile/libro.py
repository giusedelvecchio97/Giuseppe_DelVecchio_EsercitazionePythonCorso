

class Libro():
    
    
    def __unit__(self,titolo,autore,isbn):
        
        self.titolo=titolo
        self.autore=autore
        self.isbn=isbn
        
    
    
    def stampa_libro(self):
        
        print(f"Autore libro ={self.autore}, titolo del libro ={self.titolo}, codice isbn ={self.isbn}")
        
    
    
    
        
        